#2. Operadores de comparación
print( 5 == 5 ) # Igual a 
print( 5 != 5 ) # Diferente de 
print( 10 > 5 ) # Mayor que
print( 10 < 5 ) # Menor que 
print( 10 >= 5 ) # Mayor igual que
print( 10 <= 5 ) # Menor igual que 

#3. Operadores Lógicos 
v = True
f = False
#3.1. and (y)
print('-------------------------AND')
print(v and v)
print(v and f)
print(f and v)
print(f and f)
#3.2. or (o)
print('-------------------------OR')
print(v and v)
print(v and f)
print(f and v)
print(f and f)
#3.3. not (negacion)
print('-------------------------NOT')
print( not v)
print( not f)
#4. Operadores de Asignación
# suma y asigna (+=) 
print('-------------------------------+=')
edad = 20
edad += 5 
print(edad)
# resta y asigna (-=)
print('-------------------------------------------------=')
saldo = 100
saldo -= 10
print(saldo)
# multiplica y asigna (*=)
('-----------------------------------------------------*=')
precio = 30
precio *= 5
print(precio)

# divide y asigna (/=)
print('---------------------------------/=')
